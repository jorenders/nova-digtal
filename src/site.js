export const languageStorageKey = "nova-digital-language";

export const getTranslationValue = (language, key, dictionary = translations) =>
  key.split(".").reduce((value, segment) => value?.[segment], dictionary[language]);

export const resolveInitialLanguage = (
  storage,
  storageKey = languageStorageKey,
  fallbackLanguage = "nl"
) => {
  const storedLanguage = storage?.getItem(storageKey);
  return storedLanguage && translations[storedLanguage] ? storedLanguage : fallbackLanguage;
};

export const applyLanguage = (
  language,
  {
    documentObj = document,
    dictionary = translations,
    translatableNodes = documentObj.querySelectorAll("[data-i18n]"),
    translatableAttrs = documentObj.querySelectorAll("[data-i18n-attr]"),
    languageButtons = documentObj.querySelectorAll(".language-toggle"),
  } = {}
) => {
  const copy = dictionary[language] || dictionary.nl;

  documentObj.documentElement.lang = language;
  documentObj.title = copy.meta.title;

  translatableNodes.forEach((node) => {
    const value = getTranslationValue(language, node.dataset.i18n, dictionary);
    if (typeof value === "string") {
      node.textContent = value;
    }
  });

  translatableAttrs.forEach((node) => {
    const attribute = node.dataset.i18nAttr;
    const key = node.dataset.i18nKey;
    const value = getTranslationValue(language, key, dictionary);

    if (typeof value === "string") {
      node.setAttribute(attribute, value);
    }
  });

  languageButtons.forEach((button) => {
    const isActive = button.dataset.language === language;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });
};

export const switchLanguage = (
  language,
  {
    windowObj = window,
    documentObj = document,
    storageKey = languageStorageKey,
    dictionary = translations,
  } = {}
) => {
  documentObj.body.classList.add("is-translating");
  applyLanguage(language, { documentObj, dictionary });
  windowObj.localStorage.setItem(storageKey, language);
  windowObj.setTimeout(() => {
    documentObj.body.classList.remove("is-translating");
  }, 220);
};

export const initRevealObserver = (nodes, { windowObj = window } = {}) => {
  if (typeof windowObj.IntersectionObserver !== "function") {
    nodes.forEach((node) => {
      node.classList.add("is-visible");
    });
    return null;
  }

  const observer = new windowObj.IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("is-visible");
          observer.unobserve(entry.target);
        }
      });
    },
    {
      threshold: 0.18,
      rootMargin: "0px 0px -8% 0px",
    }
  );

  nodes.forEach((node, index) => {
    node.style.transitionDelay = `${Math.min(index * 80, 320)}ms`;
    observer.observe(node);
  });

  return observer;
};

export const initSite = (
  {
    windowObj = window,
    documentObj = document,
    dictionary = translations,
    storageKey = languageStorageKey,
  } = {}
) => {
  const revealNodes = documentObj.querySelectorAll(".reveal");
  const languageButtons = documentObj.querySelectorAll(".language-toggle");

  const initialLanguage = resolveInitialLanguage(windowObj.localStorage, storageKey);

  applyLanguage(initialLanguage, {
    documentObj,
    dictionary,
    languageButtons,
  });

  languageButtons.forEach((button) => {
    button.addEventListener("click", () => {
      const nextLanguage = button.dataset.language;
      if (nextLanguage && nextLanguage !== documentObj.documentElement.lang) {
        switchLanguage(nextLanguage, {
          windowObj,
          documentObj,
          storageKey,
          dictionary,
        });
      }
    });
  });

  initRevealObserver(revealNodes, { windowObj });
};
